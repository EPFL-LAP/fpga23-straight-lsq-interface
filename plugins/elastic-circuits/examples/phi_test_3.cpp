//------------------------------------------------------------------------
// If loop
//------------------------------------------------------------------------

int if_loop(int a[], int b[]) {
    int i;
    int dist;
    int sum = 1000;

    for (i = 0; i < 10; i++) {
        dist = a[i] - b[i];

        if (dist >= 0) {

            sum = (sum / dist);
        }
    }
    return sum;
}
