void while_loop(int a[], int b[], int c[]) {
    int i     = 0;
    int bound = 1000;
    int sum   = 0;

    for (i = 0; i < 1000; i++) {
        sum = (a[i] * b[i]);
        if (sum == c[i]) {
            c[i] = c[i] * 5;
            break;
        }
    }
}
