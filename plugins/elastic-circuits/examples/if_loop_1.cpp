//------------------------------------------------------------------------
// If loop
//------------------------------------------------------------------------

int if_loop(int a[], int n) {
    int i;
    int tmp;
    int c   = 5;
    int sum = 0;

    for (i = 0; i < n; i++) {
        tmp = a[i] * c;

        if (tmp > 10) {

            sum = tmp + sum;
        }
    }

    return sum;
}
