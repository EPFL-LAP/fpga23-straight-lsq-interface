void order_test(int Out[10]) {
    int i, j;
    int tmp;

    for (i = 0; i < 10; i++) {
        tmp = i;

        for (j = i; j < 10; j++) {
            tmp *= tmp;
        }
        Out[i] = tmp;
    }
}
